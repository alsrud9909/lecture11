##
## 1) 데이터베이스 생성
##
CREATE database IF NOT EXISTS 인사급여;
USE 인사급여;

##
## 2) 테이블 생성
##
CREATE TABLE 부서
(부서코드           VARCHAR(2)     NOT NULL
,부서명             VARCHAR(10)    NOT NULL
,지역               VARCHAR(10)    NULL
);

CREATE TABLE 사원
(사원번호           INT(3)       NOT NULL
,사원명             VARCHAR(10)    NOT NULL
,부서코드           VARCHAR(2)     NOT NULL
,직급               VARCHAR(6)     NOT NULL
,입사일자           DATE            NOT NULL
,퇴사일자           DATE            NULL
,급여구분           VARCHAR(4)     NOT NULL
,월급여             INT(10)      NOT NULL
,등록일시           DATETIME           NOT NULL
);

CREATE TABLE 발령이력
(사원번호           INT(3)       NOT NULL
,이력순번           INT(2)       NOT NULL
,발령일자           DATE            NOT NULL
,직급               VARCHAR(6)     NOT NULL
,부서코드           VARCHAR(2)     NOT NULL
);

CREATE TABLE 연봉제급여
(사원번호           INT(3)       NOT NULL
,연봉액             INT(10)      NOT NULL
,월기본급           INT(10)      NOT NULL
);

CREATE TABLE 호봉제급여
(사원번호           INT(3)       NOT NULL
,호봉               INT(10)      NOT NULL
,월기본급           INT(10)      NOT NULL
);

CREATE TABLE 금액제급여
(사원번호           INT(3)       NOT NULL
,월기본급           INT(10)      NOT NULL
);

CREATE TABLE 기타급여
(사원번호           INT(3)       NOT NULL
,기본수당지급여부   VARCHAR(1)     NULL
,기본수당액         INT(10)      NULL
,특별수당지급여부   VARCHAR(1)     NULL
,특별수당액         INT(10)      NULL
,성과급지급여부     VARCHAR(1)     NULL
,성과급             INT(10)      NULL
);

CREATE TABLE 급여지급
(급여월             VARCHAR(6)     NOT NULL
,사원번호           INT(3)       NOT NULL
,월급여             INT(10)      NOT NULL
,기본급             INT(10)      NOT NULL
,수당               INT(10)      NULL
,성과급             INT(10)      NULL
);

ALTER TABLE 부서       ADD CONSTRAINT 부서_PK       PRIMARY KEY(부서코드         );
ALTER TABLE 사원       ADD CONSTRAINT 사원_PK       PRIMARY KEY(사원번호         );
ALTER TABLE 발령이력   ADD CONSTRAINT 발령이력_PK   PRIMARY KEY(사원번호,이력순번);
ALTER TABLE 연봉제급여 ADD CONSTRAINT 연봉제급여_PK PRIMARY KEY(사원번호         );
ALTER TABLE 호봉제급여 ADD CONSTRAINT 호봉제급여_PK PRIMARY KEY(사원번호         );
ALTER TABLE 금액제급여 ADD CONSTRAINT 금액제급여_PK PRIMARY KEY(사원번호         );
ALTER TABLE 기타급여   ADD CONSTRAINT 기타급여_PK   PRIMARY KEY(사원번호         );
ALTER TABLE 급여지급   ADD CONSTRAINT 급여지급_PK   PRIMARY KEY(급여월,사원번호  );


